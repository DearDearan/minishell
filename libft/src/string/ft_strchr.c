#include "ft_string.h"

char	*ft_strchr(const char *s, int c)
{
	if (!s)
		return (NULL);
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		else
			s++;
	}
	if ((*s | (char)c) == '\0')
		return ((char *)s);
	return (NULL);
}
